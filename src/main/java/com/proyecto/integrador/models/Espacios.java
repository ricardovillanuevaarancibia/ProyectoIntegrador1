package com.proyecto.integrador.models;

public class Espacios {

    public String CasilleroNumero;

    public String Departamento;
    public String Provincia;

    public Espacios(String casilleroNumero, String departamento, String provincia, String distrito, String tienda) {
        CasilleroNumero = casilleroNumero;
        Departamento = departamento;
        Provincia = provincia;
        Distrito = distrito;
        Tienda = tienda;
    }

    public String Distrito;
    public String Tienda;

}
